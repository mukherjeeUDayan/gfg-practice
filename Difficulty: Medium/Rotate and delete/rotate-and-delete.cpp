class Solution {
  public:
    int rotateDelete(vector<int> &v) {
        // Your code here
        int n=v.size();
        int k=1;
        while(k <= n){
                   rotate(v.begin(), v.begin()+v.size()-1, v.end());
                   v.erase(v.begin()+n-k);
                   n = v.size();
                   k++;
               }
        return v[0];
    }
};


