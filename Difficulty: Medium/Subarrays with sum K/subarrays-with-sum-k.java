// User function Template for Java
class Solution {
    public int countSubarrays(int arr[], int k) {
        // code here
        HashMap<Integer,Integer> mp = new HashMap<>();
        mp.put(0,1);
        int sum=0 ,answ=0;
        for(int val:arr){
            sum+=val;
            if(mp.containsKey(sum-k))
                answ+=mp.get(sum-k);
            if(mp.containsKey(sum))
                mp.put(sum,mp.get(sum)+1);
            else
                mp.put(sum,1);
        }
        return answ;
    }
}